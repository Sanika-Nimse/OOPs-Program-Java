
public class Student {
int roll;
String name;
float marks;

public void setRoll (int r) {
	roll=r;
}
public void setName (String n) {
	name=n;
	
}
public void setMarks (float m) {
	marks=m;
}
public int getRoll() {
	return roll;
}
public String getName() {
	return name;
} 
public float getMarks() {
	return marks;
}
}
